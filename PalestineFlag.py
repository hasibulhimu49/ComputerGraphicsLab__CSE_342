import turtle

screen=turtle.Screen()
screen.setup(1000,700)

t=turtle.Turtle()
turtle.speed(0)
t.fillcolor("black")
t.begin_fill()
t.penup()
t.goto(-250,-100)
t.pendown()
t.forward(500)
t.left(90)
t.forward(100)
t.left(90)
t.forward(500)
t.left(90)
t.forward(100)
t.left(90)
t.end_fill()

t.fillcolor("white")
t.begin_fill()
